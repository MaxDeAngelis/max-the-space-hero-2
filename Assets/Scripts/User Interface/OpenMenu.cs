using UnityEngine;
using System.Collections;

public class OpenMenu : MonoBehaviour {
	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/// 								     		PUBLIC VARIABLES											     ///
	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	public MENU_TYPE menuToActivate;	// The menu game object to activate on enter of the collider

	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/// 								     		PUBLIC FUNCTIONS											     ///
	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>
	/// Open this instance of the shop.
	/// </summary>
	public void open() {
		MenuManager.Instance.showMenu(menuToActivate);
	}

	/// <summary>
	/// Handles changing the cursor to a pointer
	/// </summary>
	public void mouseEnter() {
		CursorManager.Instance.setCursorToPointer();
	}

	/// <summary>
	/// Handles resetting the cursor
	/// </summary>
	public void mouseLeave() {
		CursorManager.Instance.resetCursor();
	}
}
