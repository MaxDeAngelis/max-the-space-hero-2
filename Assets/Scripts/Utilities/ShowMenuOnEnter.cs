using UnityEngine;
using System.Collections;

[RequireComponent (typeof (BoxCollider2D))]

public class ShowMenuOnEnter : MonoBehaviour {
	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/// 								     		PUBLIC VARIABLES											     ///
	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	public GameObject menuToActivate;	// The menu game object to activate on enter of the collider

	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/// 								     		PRIVATE FUNCTIONS											     ///
	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	/**
	* @private Handles checking if this game object is intersecting with the player and if so show the menu 
	**/
	void OnTriggerEnter2D(Collider2D otherCollider) {
		if (otherCollider.gameObject.tag == "Player") {
			GameManager.Instance.showMenu(menuToActivate);
		}
	}
}
