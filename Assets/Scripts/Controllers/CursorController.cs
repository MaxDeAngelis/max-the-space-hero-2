using UnityEngine;
using System.Collections;

public class CursorController : MonoBehaviour {
	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/// 								     		PUBLIC VARIABLES											     ///
	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	public CURSOR_TYPE cursorType = CURSOR_TYPE.Default;

	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/// 								     		PRIVATE FUNCTIONS											     ///
	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	void OnMouseEnter() {
		GameManager.Instance.setCursor(cursorType);
	}
	void OnMouseExit() {
		GameManager.Instance.resetCursor();
	}

}
